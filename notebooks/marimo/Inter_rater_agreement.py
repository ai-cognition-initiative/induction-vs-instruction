@app.cell
def _(judges, pd):
    from itertools import combinations
    from sklearn.metrics import cohen_kappa_score

    def _pairwise(df: pd.DataFrame) -> pd.DataFrame:
        pivot = df.pivot_table(
            index="sample_id",
            columns="judge_id",
            values="classification",
            aggfunc="first",
        )
        pivot = pivot.dropna()
        judges_list = sorted(pivot.columns)
        rows = []
        for a, b in combinations(judges_list, 2):
            try:
                k = cohen_kappa_score(pivot[a], pivot[b])
            except Exception:
                k = None
            rows.append({"judge_a": a, "judge_b": b, "cohen_kappa": k, "n": len(pivot)})
        return pd.DataFrame(rows)

    pairwise_overall = _pairwise(judges)
    pairwise_by_condition = (
        judges.groupby("condition", group_keys=False)
        .apply(_pairwise)
        .reset_index(level=0)
    )

    pairwise_overall
    return cohen_kappa_score, pairwise_by_condition, pairwise_overall


