@app.cell
def _(judges, pd):
    _recomputed = (
        judges.groupby("sample_id")["classification"]
        .apply(lambda s: s.nunique() == 1)
        .rename("unanimous_recomputed")
    )
    _stored = judges.groupby("sample_id")["unanimous"].first()
    unanimous_check = pd.concat([_stored, _recomputed], axis=1)
    unanimous_check["match"] = (
        unanimous_check["unanimous"].astype("boolean")
        == unanimous_check["unanimous_recomputed"]
    )
    print(f"Stored unanimous rate:     {_stored.mean():.3f}")
    print(f"Recomputed unanimous rate: {_recomputed.mean():.3f}")
    print(f"Per-sample match rate:     {unanimous_check['match'].mean():.3f}")
    unanimous_check.head()
